package com.vcs.floor.java;

public class Floor {
	private int floor_Id;
	private String floor_Name;
	private String floor_Desc;

	public int setFloor_Id(int floor_Id) {
		return floor_Id;
	}

	public int getFloor_Id() {
		return floor_Id;
	}

	public String setFloor_Name(String floor_Name) {
		return floor_Name;
	}

	public String getFloor_Name() {
		return floor_Name;
	}

	public String setFloor_Desc(String floor_Desc) {
		return floor_Desc;
	}

	public String getFloor_Desc() {
		return floor_Desc;
	}

}
